    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
//        processRequest(request, response);
        response.sendRedirect("register.jsp");
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
//        processRequest(request, response);
        request.setCharacterEncoding("UTF-8");
        String name = request.getParameter("name");
        String user = request.getParameter("user");
        String pass = request.getParameter("pass");
        String email = request.getParameter("email");
        String phone = request.getParameter("phone");
        String address = request.getParameter("add");
        CustomerDao d = new CustomerDao();
        Customers c = d.checkUser(user);
        if (c != null) {
            String err = "Username are exists!";
            request.setAttribute("err", err);
            request.getRequestDispatcher("register.jsp").forward(request, response);
        } else if (user.trim().length() == 0 || pass.trim().length() == 0) {
            String err = "You must fill in full of the blank below before!";
            request.setAttribute("err", err);
            request.getRequestDispatcher("register.jsp").forward(request, response);
        }
        else {
            int i = d.getAll().size()+1;
            c = new Customers(i, name, user, pass, email, phone, address);
            d.insert(c, i);
            HttpSession ses = request.getSession();
            ses.setAttribute("acc", c);
            response.sendRedirect("home");
        }
    }

